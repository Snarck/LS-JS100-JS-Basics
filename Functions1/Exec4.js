/*

Squared Number

Write a function that accepts a single 
argument, a number, and returns the result of multiplying its argument by an exponent of 2 (also known as squaring the number).

*/

function squaredNumber(num) {
  return num ** num;	
};

//console log is so I can see the output.  
console.log(squaredNumber(3)); // 9
squaredNumber(3);