let weather = "cloudy";

switch (weather) {
	case "sunny":
		console.log("It's sunny");
		break;
	case "rainy":
		console.log("it's rainy");
		break;
	case "cloudy":
		console.log("It's cloudy");
		break;
	case "windy":
		console.log("It's windy");
		break;
}